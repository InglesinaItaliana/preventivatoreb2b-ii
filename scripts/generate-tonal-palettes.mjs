#!/usr/bin/env node
/**
 * Generates M3 tonal palettes (18 tones) for SIDERA suite module colors.
 * Uses @material/material-color-utilities — same algorithm as the official
 * Material Theme Builder (HCT color space).
 *
 * Output: CSS custom properties printed to stdout AND written to
 * scripts/tonal-palettes-output.css for copy/paste into src/style.css.
 *
 * Usage:
 *   node scripts/generate-tonal-palettes.mjs
 *
 * Re-run any time the key colors change in docs/ATLAS.md sez. 2.
 */

import { argbFromHex, hexFromArgb, TonalPalette } from '@material/material-color-utilities';
import { writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));

// Key colors from docs/ATLAS.md sez. 2 (Crab Nebula palette, on-light values)
const MODULES = [
  { name: 'sidera',   hex: '#D4C498' },
  { name: 'pulsar',   hex: '#3A8C80' },
  { name: 'cepheid',  hex: '#C4941C' },
  { name: 'nebula',   hex: '#B85425' },
  { name: 'nova',     hex: '#5C6822' },
  { name: 'magnetar', hex: '#7A4830' },
  { name: 'quasar',   hex: '#98C0D0' },
];

// M3 standard tones, matching the Material Theme Builder export format.
const TONES = [0, 5, 10, 15, 20, 25, 30, 35, 40, 50, 60, 70, 80, 90, 95, 98, 99, 100];

function paletteForKeyColor(hex) {
  const argb = argbFromHex(hex);
  const palette = TonalPalette.fromInt(argb);
  return Object.fromEntries(
    TONES.map((t) => [t, hexFromArgb(palette.tone(t)).toUpperCase()])
  );
}

function formatCssBlock(name, palette) {
  const lines = TONES.map((t) => {
    const tonePadded = String(t).padStart(3, ' ');
    return `  --md-ref-palette-${name}-${t}: ${palette[t]};`.replace(`-${t}: `, `-${tonePadded.trim()}: `);
  });
  return `  /* ── ${name.toUpperCase()} tonal palette (M3, 18 tones) ── */\n${lines.join('\n')}`;
}

const header = `/* Auto-generated by scripts/generate-tonal-palettes.mjs — DO NOT EDIT BY HAND.
   Source: docs/ATLAS.md sez. 2 (Crab Nebula key colors).
   Re-run the script to regenerate. M3 spec: https://m3.material.io/styles/color/system */`;

const blocks = MODULES.map(({ name, hex }) => {
  const palette = paletteForKeyColor(hex);
  return `  /* key color (on-light from ATLAS sez. 2): ${hex} */\n` + formatCssBlock(name, palette);
});

const css = `${header}\n:root {\n${blocks.join('\n\n')}\n}\n`;

const outPath = resolve(__dirname, 'tonal-palettes-output.css');
writeFileSync(outPath, css, 'utf8');

console.log(css);
console.log(`\n✓ Written to ${outPath}`);
console.log(`  ${MODULES.length} modules × ${TONES.length} tones = ${MODULES.length * TONES.length} variables`);

// Sanity check: compare generated tones 40/80 to our current on-light / on-dark values
const CURRENT_ON_LIGHT_ON_DARK = {
  pulsar:   { onLight: '#3A8C80', onDark: '#3AAF98' },
  cepheid:  { onLight: '#C4941C', onDark: '#D4A020' },
  nebula:   { onLight: '#B85425', onDark: '#C46030' },
  nova:     { onLight: '#5C6822', onDark: '#8FAB35' },
  magnetar: { onLight: '#7A4830', onDark: '#B06842' },
};

console.log('\n── Sanity check: current on-light/on-dark vs M3 tone 40/80 ──');
console.log('module     | on-light  → tone 40   | on-dark   → tone 80');
console.log('-----------|-----------------------|----------------------');
for (const [name, { onLight, onDark }] of Object.entries(CURRENT_ON_LIGHT_ON_DARK)) {
  const palette = paletteForKeyColor(onLight);
  const t40 = palette[40];
  const t80 = palette[80];
  console.log(
    `${name.padEnd(10)} | ${onLight} → ${t40}    | ${onDark} → ${t80}`
  );
}
